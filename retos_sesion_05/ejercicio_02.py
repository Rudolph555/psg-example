#2. Escribe un programa en Python que convierta las siguientes temperaturas de grados Celsius a 
#grados Fahrenheit:
#30 ºC
#-273.99 ºC
#100 ºC

grado1Celsius = 30
grado2Celsius = -273.99
grado3Celsius = 100

grado1Fahrenheit = (grado1Celsius *(9/5))+32
grado2Fahrenheit = (grado2Celsius *(9/5))+32
grado3Fahrenheit = (grado3Celsius *(9/5))+32

print(grado1Fahrenheit,"º Fahrenheit")
print(grado2Fahrenheit,"º Fahrenheit")
print(grado3Fahrenheit,"º Fahrenheit")